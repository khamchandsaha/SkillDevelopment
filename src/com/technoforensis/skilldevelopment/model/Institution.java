package com.technoforensis.skilldevelopment.model;

public class Institution {

	int institute_id;
	String institute_name,established_year,mobile,address,registration,email,institute_logo_url,institute_website;
	public int getInstitute_id() {
		return institute_id;
	}
	public void setInstitute_id(int institute_id) {
		this.institute_id = institute_id;
	}
	public String getInstitute_name() {
		return institute_name;
	}
	public void setInstitute_name(String institute_name) {
		this.institute_name = institute_name;
	}
	public String getEstablished_year() {
		return established_year;
	}
	public void setEstablished_year(String established_year) {
		this.established_year = established_year;
	}
	public String getMobile() {
		return mobile;
	}
	public void setMobile(String mobile) {
		this.mobile = mobile;
	}
	public String getAddress() {
		return address;
	}
	public void setAddress(String address) {
		this.address = address;
	}
	public String getRegistration() {
		return registration;
	}
	public void setRegistration(String registration) {
		this.registration = registration;
	}
	public String getEmail() {
		return email;
	}
	public void setEmail(String email) {
		this.email = email;
	}
	public String getInstitute_logo_url() {
		return institute_logo_url;
	}
	public void setInstitute_logo_url(String institute_logo_url) {
		this.institute_logo_url = institute_logo_url;
	}
	public String getInstitute_website() {
		return institute_website;
	}
	public void setInstitute_website(String institute_website) {
		this.institute_website = institute_website;
	}
	
}
